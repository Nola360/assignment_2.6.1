#Declaring value of temperature
temperature = 0.0

#Redecalring value of temperature
temperature = float(input('Enter a temperature in Celsius. '))

#Declaring value of C - Celsius
C = temperature

#Declaring of value F - Farhenheit formula
F = (9/5) * C + 32


print(f"Degrees in Celsius: {temperature}°C")
print(f"Degrees in Farhenheit: {F:.0f}°F")
